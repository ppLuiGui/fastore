    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        DetalleProducto frmDetalleProducto = new DetalleProducto();
        
        if(!frmDetalleProducto.isVisible()){
            
            JDesktopPane escritorio = (JDesktopPane) getParent();
            escritorio.add(frmDetalleProducto);
            frmDetalleProducto.toFront();
            frmDetalleProducto.setVisible(true);
            
        }
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        
        int selectedIndex = tblProductos.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel) tblProductos.getModel();
        DetalleProducto frmDetalleProducto = new DetalleProducto();
        
        // Con este IF detectamos si hizo doble click en algun producto
        if (evt.getClickCount() == 2 && selectedIndex != -1) {
            
            // Obtenemmos que producto quiere editar el Proveedor
            
            int productoId = Integer.valueOf( dtm.getValueAt(selectedIndex, 0).toString() );
            
            JDesktopPane escritorio = (JDesktopPane) getParent();
            escritorio.add(frmDetalleProducto);
            frmDetalleProducto.establecerProducto(productoId);
            frmDetalleProducto.toFront();
            frmDetalleProducto.setVisible(true);
            
        }
        
        
        
    }//GEN-LAST:event_tblProductosMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        DefaultTableModel dtm = (DefaultTableModel) tblProductos.getModel();
        
        
        if(dtm.getRowCount() > 0) {
            if( !tblProductos.getSelectionModel().isSelectionEmpty()) {
                 
                 int selectedIndex = tblProductos.getSelectedRow();
                 int productoId = Integer.valueOf( dtm.getValueAt(selectedIndex, 0).toString() );
                 
                int opcion = JOptionPane.showConfirmDialog(null, "Esta seguro de que quiere eliminar este producto?");
                // 0=yes, 1=no, 2=cancel
                if(opcion == 0){
                    ModeloProducto mProducto = new ModeloProducto();
                    mProducto.eliminarProducto(productoId);
                    
                    
                    JOptionPane.showMessageDialog(null, "Producto Eliminado");
                    dispose();

                }else{
                    JOptionPane.showMessageDialog(null, "Producto No Modificado");
                }  
                 
                 
                 
                 
                 
                 
                 
                 
                 
            }else{
                
                // Llegamos aqui cuando hay pedidos en la lista pero no hay seleccion
                 JOptionPane.showMessageDialog(null, "Seleccione un Producto Primero");
            }
        }else{
            // Llegamos aqui cuando no hay pedidos en la lista
            JOptionPane.showMessageDialog(null, "Seleccione un Producto Primero");
        }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed


