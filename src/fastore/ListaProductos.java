    private void listarProductos(){
        
        ModeloProducto mProducto = new ModeloProducto();
        ArrayList<ProductoDTO> listaProductos = mProducto.listarTodos();
        
        DefaultTableModel dtm = (DefaultTableModel) tblProductos.getModel();
        dtm.setRowCount(0);
        
        for( ProductoDTO producto : listaProductos ) {
           
            Object[] fila = new Object[5];
        
            fila[0] = producto.getId_producto();
            fila[1] = producto.getClave();
            fila[2] = producto.getNombre();
            fila[3] = producto.getPrecio();
            fila[4] = producto.getUnidad();

            dtm.addRow(fila);
        }
        
    }
    
    
    
