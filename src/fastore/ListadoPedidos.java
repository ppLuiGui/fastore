    public void generarPdf(){
         DefaultTableModel dtm = (DefaultTableModel) tablaPedidos.getModel();
        
        
        if(dtm.getRowCount() > 0) {
            if( !tablaPedidos.getSelectionModel().isSelectionEmpty()) {
                 
                 int selectedIndex = tablaPedidos.getSelectedRow();
                 int pedidoId = Integer.valueOf( dtm.getValueAt(selectedIndex, 0).toString() );
                 
                 
                 
                 JOptionPane.showMessageDialog(null, "Generar PDF del pedido: " + pedidoId);
                 
                 
                 
            }else{
                
                // Llegamos aqui cuando hay pedidos en la lista pero no hay seleccion
                 JOptionPane.showMessageDialog(null, "Seleccione un Pedido Primero");
            }
        }else{
            // Llegamos aqui cuando no hay pedidos en la lista
            JOptionPane.showMessageDialog(null, "Seleccione un Pedido Primero");
        }
        
        
    }

