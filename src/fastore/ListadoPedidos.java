    public void listarPedidos() {
        
        int proveedorId = UsuarioDTO.getId();
        
        DefaultTableModel dtm = (DefaultTableModel) tablaPedidos.getModel();
        dtm.setRowCount(0);
        ModeloPedido mPedido = new ModeloPedido();
        ArrayList<PedidoDTO> pedidos = mPedido.listaPorProveedor(proveedorId);
        
        
        
        for( PedidoDTO pedido : pedidos ) {
           
            Object[] fila = new Object[4];
        
            fila[0] = pedido.getId_pedido();
            fila[1] = pedido.getFecha();
            fila[2] = pedido.getTotal();
            fila[3] = pedido.getStatus();

            dtm.addRow(fila);
        }
        
    }
    
    
    
    
