    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int proveedorId = ddProveedor.getItemAt( ddProveedor.getSelectedIndex()).getValue();
        ComentarioDTO comentarioDTO = new ComentarioDTO();
        
        Date fechaHoySinFormato = new Date();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");  
        String fechaHoy= formatter.format(fechaHoySinFormato);  
        
        comentarioDTO.setComentario(  txtComentario.getText()  );
        comentarioDTO.setCliente_id(  UsuarioDTO.getId() );
        comentarioDTO.setProveedor_id( proveedorId );
        comentarioDTO.setLeido(0);
        comentarioDTO.setFecha( fechaHoy );
        
        
        ModeloComentario mComentario = new ModeloComentario();
        mComentario.guardarComentario(comentarioDTO);
        
        
        JOptionPane.showMessageDialog(null, "Tu Comentario se envio Exitosamente");
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed


