    private void llenarComboProveedores(){
        
        ArrayList<ProveedorDTO> listProveedores = null;
        ModeloProveedor mdlPrv = new ModeloProveedor();
        listProveedores = mdlPrv.obtenerLista();
        
        ddProveedor.addItem( new ComboItem( "Seleccione uno...", 0));
        
        for (ProveedorDTO proveedor : listProveedores) {    
            
            String PrvNombre = proveedor.getNombre();
            int PrvId = proveedor.getId();
            
            // Ponemos la primer letra en mayuscula y las demas en minuisculas
            PrvNombre = PrvNombre.substring(0, 1).toUpperCase() + PrvNombre.substring(1).toLowerCase();
            
            
            ddProveedor.addItem(  new ComboItem(PrvNombre, PrvId));
        }
        
    }
    
    
    
