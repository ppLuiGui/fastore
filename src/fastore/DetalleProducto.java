    public void establecerProducto( int productoId ){
        
        this.estadoDetalleProducto = "MODIFICAR";
        btnAgregar.setVisible(false);
        btnModificar.setVisible(true);
        btnLimpiar.setVisible(false);
        
        cargarDetallesProducto(productoId);
        this.productoId = productoId;
        
    }
    
    private void cargarDetallesProducto(int productoId) {
        
        ModeloProducto mProducto = new ModeloProducto();
        ProductoDTO producto = mProducto.buscarProductoPorId(productoId);
        
        txtClave.setText( producto.getClave() );
        txtNombre.setText( producto.getNombre() );
        txtPrecio.setText( String.valueOf(producto.getPrecio())  );
        txtdetalles.setText( producto.getDetalles() );
        
        
        ModeloCategorias mCategoria = new ModeloCategorias();
        CategoriaDTO categoriaDTO = mCategoria.buscarPorId(producto.getCategoria_id());
        

        seleccionarCategoria(categoriaDTO.getNombre());
        
    }
    
    
    private void seleccionarCategoria(String categoriaNombre) {
        
        ComboItem item;
        for (int i = 0; i < ddCategoria.getItemCount(); i++)
        {
            item = (ComboItem)ddCategoria.getItemAt(i);
            if (item.getKey().equalsIgnoreCase(categoriaNombre))
            {
                ddCategoria.setSelectedIndex(i);
                break;
            }
        }
        
      
    }
    

    public void llenarCategorias(){
        
        ArrayList<CategoriaDTO> listCategorias = null;
        ModeloCategorias mCategorias = new ModeloCategorias();
        
        
        listCategorias = mCategorias.obtenerLista();
        
        ddCategoria.addItem( new ComboItem( "Seleccione una...", 0));
        
        for (CategoriaDTO categoria : listCategorias) {    
            
            String PrvNombre = categoria.getNombre();
            int PrvId = categoria.getId_categoria();
            
            
            ddCategoria.addItem(  new ComboItem(PrvNombre, PrvId));
        }
        
    }
    
