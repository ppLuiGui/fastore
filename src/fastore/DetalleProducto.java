    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        
        ProductoDTO producto = new ProductoDTO();
        
        int categoriaId = ddCategoria.getItemAt( ddCategoria.getSelectedIndex()).getValue();
        
        producto.setClave( txtClave.getText() );
        producto.setNombre( txtNombre.getText());
        producto.setDetalles( txtdetalles.getText());
        producto.setPrecio( Double.valueOf( txtPrecio.getText() )  );
        producto.setCategoria_id( categoriaId );
        producto.setProveedor_id( UsuarioDTO.getId() );
        
        
        ModeloProducto mProducto = new ModeloProducto();
        mProducto.guardarProducto(producto);
        
        JOptionPane.showMessageDialog(null, "Producto Almacenado Satisfactoriamente");
        dispose();
        
    }//GEN-LAST:event_btnAgregarActionPerformed

